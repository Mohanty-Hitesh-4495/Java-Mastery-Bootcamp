package ExamPrep.Module1;// Write a java program to demonstrate the uses of the primitive data types.
import java.util.Scanner;
public class InputDatatype {
    public static void main(String[] args) {
//        Scanner sc = new Scanner(System.in);

        byte num1 = 2;
        short num2 = 52;
        int num3 = 59565464;
        long num4 = 65498499498L;
        float num5 = 56.2f;
        double num6 = 465.165985;
        char ch = 'k';
        boolean bo = true;
        System.out.println("BYTE = "+num1);
        System.out.println("SHORT = "+ num2);
        System.out.println("INTEGER = "+ num3);
        System.out.println("LONG = "+ num4);
        System.out.println("FLOAT = "+num5);
        System.out.println("DOUBLE = "+num6);
        System.out.println("CHAR = "+ch);
        System.out.println("BOOLEAN = "+bo);

//        sc.close();
    }
}
